// Arithmatic Operator
let a = 10;
let b =  4;
console.log("a + b =", a+b);
// output: a + b = 14
console.log("a - b =", a-b);
// output: a - b = 6
console.log("a / b =", a/b);
// output: a / b = 2.5
console.log("a * b =", a*b);
// output: a * b = 240
console.log("a ** b =", a**b); // note: a**b , it means a to the power b
// output: a ** b = 10000
console.log("a % b =", a%b); // note : a%b, it means its shows the remainder
// output: a % b = 2

// Increment operator and Decrement operator
console.log("a++ =", a++); // note : a++ means first print the value of a and then increment the value of a ..
// output: a++ = 10
console.log("a =", a);
// output: a = 11
console.log("++a =", ++a); // note: ++a means first increment the value of a and then print the value of a ..
// output: 12

// Example---
let c = 10;
console.log("++c =", ++c); // val c = 11
// output: ++c = 11
console.log("c++ =", c++); // val c = 12
// output: c++ = 11
console.log("--c =", --c); // val c = 11
// output: --c = 11
console.log("c-- =", c--); // val c = 10
// output: c-- = 11
console.log("c =", c);
// output: c = 10
console.log("c-- =", c--); 
// output: c-- = 10

