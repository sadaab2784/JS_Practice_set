// Comparison Operator
let comp1 = 6;
let comp2 = "6";
console.log("comp1 == comp2 is", comp1 == comp2);
// output: comp1 == comp2 is true
console.log("comp1 != comp2 is", comp1 != comp2);
// output: comp1 != comp2 is false
console.log("comp1 === comp2 is", comp1 === comp2); // note: "===" means that both type and value are same ..
// output: comp1 === comp2 is false
console.log("comp1 !== comp2 is", comp1 !== comp2);
// output: comp1 !== comp2 is ture

